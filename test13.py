# Create a dictionary of 5 countries and their capitals, then print all key-value pairs.


countries_capitals = {
    "India": "New Delhi",
    "United States": "Washington, D.C.",
    "United Kingdom": "London",
    "Canada": "Ottawa",
    "Australia": "Canberra",
    "Germany": "Berlin",
    "France": "Paris",
    "Japan": "Tokyo",
    "China": "Beijing",
    "Brazil": "Brasília",
    "Russia": "Moscow",
    "Italy": "Rome"
}


for [country, capital] in countries_capitals.items():
    print(f"Capital of {country} is {capital}")



